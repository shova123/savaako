                <strong>Some peculiar requests provided for the software were :</strong>
                <ul>
                    <li>Alert system (signup) that will alert about progress of event (when Internet connected)</li>
                    <li>Analytics to be done based on different aspects - registry form dynamic creation</li>
                    <li>posting to twitter using a hashtag by the controller, track the hashtag during event on presentator screen</li>
                    <li>Share & collate photos from multiple phones</li>
                </ul><br>
                <p>With the above scenarios and peculiar requests, Savaako is designed as a
                    tool to be a cross among rapporteur, event organizer, marshal, record
                    keeper and data analyzer. It tries to capture the workflow and
                    documentation of a generic seminar, conference or a meeting along with
                    invitation, registration and customized report dissemination. Savaako
                    looks at 3 aspects : pre-event, during event and post event. Savaako
                    integrates : data analysis and customized reporting.
                </p>
                <p>
                    In pre-event Savaako takes care of publication of the event, provide
                    poster and early details and reading material for the event, allow
                    potential participant registration and analysis of potential
                    participation history of potential participants.
                </p>
                <p>
                    In during-event Savaako will manage interaction, Q&A, Polls, Surveys and
                    presentations of the program. It will capture the overall interaction of
                    the event along with socail media integration. It will also look to
                    facilitate and capture the general sense of the event.
                </p>
                <p>
                    In post-event Savaako will generate report and analyze interactions to
                    convert into knowledge for future events. Reports will be customizable
                    to fit the purpose of the event.
                </p>
                <p>
                    The philosophy of Savaako is to be a coordinator, rapporteur and expert
                    with simplified interactions to allow for rapid information collection,
                    management and dissemination.
                </p>
                <p>
                    The expected outcome of usage of Savaako is to help cooordinators manage
                    events better, participants interact better, takehome from events is
                    more comprehensive and accessible and knowledge is captured and made
                    ready for future usage.
                </p>
